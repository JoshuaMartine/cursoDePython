# Ejercicio utilizando operador de asignación simple
x = 5
print(x)  # Output: 5

# Ejercicio utilizando operador de asignación con suma
x = 5
x += 3
print(x)  # Output: 8

# Ejercicio utilizando operador de asignación con resta
x = 10
x -= 4
print(x)  # Output: 6


# Ejercicio utilizando operador de asignación con multiplicación
x = 3
x *= 2
print(x)  # Output: 6


# Ejercicio utilizando operador de asignación con división
x = 10
x /= 2
print(x)  # Output: 5.0


# Ejercicio utilizando operador de asignación con módulo
x = 10
x %= 3
print(x)  # Output: 1


# Ejercicio utilizando operador de asignación con exponente
x = 2
x **= 3
print(x)  # Output: 8


# Ejercicio utilizando operador de asignación con división entera
x = 10
x //= 3
print(x)  # Output: 3
