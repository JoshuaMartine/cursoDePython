# Operador de igualdad ==
a = 5
b = 5
if a == b:
    print("a es igual a b")
else:
    print("a no es igual a b")

# Operador de desigualdad !=
x = 10
y = 5
if x != y:
    print("x no es igual a y")
else:
    print("x es igual a y")

# Operador de mayor que >
m = 8
n = 3
if m > n:
    print("m es mayor que n")
else:
    print("m no es mayor que n")

# Operador de menor que <
p = 4
q = 7
if p < q:
    print("p es menor que q")
else:
    print("p no es menor que q")

# Operador de mayor o igual que >=
i = 6
j = 6
if i >= j:
    print("i es mayor o igual que j")
else:
    print("i no es mayor o igual que j")

# Operador de menor o igual que <=
u = 3
v = 5
if u <= v:
    print("u es menor o igual que v")
else:
    print("u no es menor o igual que v")
